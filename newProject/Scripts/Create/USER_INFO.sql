CREATE TABLE USER_INFO(
	EMP_CODE VARCHAR2(10) PRIMARY KEY,
	AREA_CODE VARCHAR2(10),
	EMP_NAME VARCHAR2(10) NOT NULL,
	EMP_PW VARCHAR2(20) NOT NULL,
	EMP_GENDER VARCHAR2(6) NOT NULL,
	EMP_USE VARCHAR2(2) NOT NULL,
	EMP_IMG BLOB,
	EMP_AUTH VARCHAR2(20) NOT NULL,
	CONSTRAINT "USER_INFO_FK" FOREIGN KEY ("AREA_CODE") REFERENCES LOCATION("AREA_CODE")
);


